int64_t elapsedTimeToBeidouTime(int64_t elapsedTimeMs)
{
  int64_t beidouTimeMs = (elapsedTimeMs - 14'000);

  if (beidouTimeMs < 0)
    beidouTimeMs += (60 * 60 * 24 * 7 * 1000);

  return beidouTimeMs;
}

